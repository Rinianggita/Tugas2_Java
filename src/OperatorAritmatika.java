public class OperatorAritmatika {
    public int nilaiHasil;
    
    int penjumlahan (int nilai1, int nilai2){
        nilaiHasil = nilai1 + nilai2;
        return nilaiHasil;
    }
    
    int pengurangan (int nilai1, int nilai2){
        nilaiHasil = nilai1 - nilai2;
        return nilaiHasil;
    }
    
    int perkalian (int nilai1, int nilai2){
        nilaiHasil = nilai1 * nilai2;
        return nilaiHasil;
    }
    
    double pembagian (double nilai1, double nilai2){
        double Hasil = nilai1 / nilai2;
        return nilaiHasil;
    }
}
